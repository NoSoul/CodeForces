#include <stdio.h>

int main()
{
    int r1,r2,c1,c2,d1,d2,i;
    int A[2][2];
    scanf("%d %d %d %d %d %d",&r1,&r2,&c1,&c2,&d1,&d2);
    for(i=1; i<10; ++i)
    {
        A[0][0]=i;
        A[0][1]=r1-A[0][0];
        A[1][0]=c1-A[0][0];
        A[1][1]=d1-A[0][0];
        if(A[0][0]==A[0][1]||A[0][0]==A[1][0]||A[0][0]==A[1][1]||A[0][1]==A[1][0]||A[0][1]==A[1][1]||A[1][0]==A[1][1])
        {
            continue;
        }
        if(A[0][1]<1||A[0][1]>9||A[1][0]<1||A[1][0]>9||A[1][1]<1||A[1][1]>9)
        {
            continue;
        }
        if((A[1][0]+A[1][1])==r2&&(A[0][1]+A[1][1])==c2&&A[0][1]+A[1][0]==d2)
        {
            printf("%d %d\n%d %d\n",A[0][0],A[0][1],A[1][0],A[1][1]);
            break;
        }
    }
    if(i==10)
    {
        puts("-1");
    }
    return 0;
}
