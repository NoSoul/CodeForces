#include <stdio.h>

int main()
{
    char Str[6][28] = {
        "+------------------------+",
        "|#.#.#.#.#.#.#.#.#.#.#.|D|)",
        "|#.#.#.#.#.#.#.#.#.#.#.|.|",
        "|#.......................|",
        "|#.#.#.#.#.#.#.#.#.#.#.|.|)",
        "+------------------------+"
    };
    int K, i, j, k;
    scanf("%d", &K);
    while(K)
    {
        for(j=0; j<27; ++j)
        {
            for(i=0; i<6; ++i)
            {
                if(Str[i][j] == '#')
                {
                    Str[i][j] = 'O';
                    --K;
                    if(!K)
                    {
                        for(k=0; k<6; ++k)
                        {
                            puts(Str[k]);
                        }
                        return 0;
                    }
                }
            }
        }
    }
    for(k=0; k<6; ++k)
    {
        puts(Str[k]);
    }
    return 0;
}
